package Q9057;

public class Main {

	public static void main(String[] args) {

		for (int j = 1; j < 4; j++) {
			for (int i = 0; i < j; i++) {
				System.out.print("*");
			}
			System.out.println("");
		}

		for (int j = 3; j > 0; j--) {
			for (int i = 0; i < j; i++) {
				System.out.print("*");
			}
			System.out.println("");
		}

	}

}
